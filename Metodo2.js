//Método 2: Array.prototype.concat()
//Esse array é utilizado para (mesclar/combinar) dois ou mais arrays.

//Exemplo 1: Concatenamento dois arrays.

let animais = ["Abelha", "Borboleta", "Cachorro"];
let animais2 = ["Doninha", "Elefante"];

console.log(animais.concat(animais2));

//Exemplo 2: Concatenamento valores individuais.

let numeros = ["1", "2", "3", "4", "5"];
let complementos = ["6"];

console.log(numeros.concat(complementos));
